$('.openmodal').click(function (e) {
        e.preventDefault();
        $('.modal').addClass('opened');
    });
$('.closemodal').click(function (e) {
        e.preventDefault();
        $('.modal').removeClass('opened');
    });

$('.openmodall').click(function (e) {
        e.preventDefault();
        $('.modall').addClass('opened');
    });
$('.closemodall').click(function (e) {
        e.preventDefault();
        $('.modall').removeClass('opened');
    });